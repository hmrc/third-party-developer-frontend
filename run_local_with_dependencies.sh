#!/bin/bash

if [[ -z ${DESKPRO_KEY} ]]; then
  echo Please set the environment variable DESKPRO_KEY
  exit 1
fi

sm2 -start MONGO DATASTREAM 

sm2 -start EMAIL HMRC_EMAIL_RENDERER DIGITAL_CONTACT_STUB DESKPRO_STUB DESKPRO_TICKET_QUEUE INTERNAL_AUTH

sm2 -start THIRD_PARTY_APPLICATION THIRD_PARTY_DEVELOPER API_SUBSCRIPTION_FIELDS API_DEFINITION THIRD_PARTY_DELEGATED_AUTHORITY API_PLATFORM_EVENTS API_PLATFORM_MICROSERVICE THIRD_PARTY_ORCHESTRATOR PUSH_PULL_NOTIFICATIONS_API API_PLATFORM_XML_SERVICES API_PLATFORM_ORGANISATION

sm2 -start API_PLATFORM_DESKPRO --appendArgs '{"API_PLATFORM_DESKPRO":["-Ddeskpro.api-key='${DESKPRO_KEY}'"]}'

./run_local.sh
